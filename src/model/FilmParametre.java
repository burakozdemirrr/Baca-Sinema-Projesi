package model;

public class FilmParametre {
	private String FilmAdi;
    private String FilmTuru;
    private String FilmSuresi;
    private String FilmFragmani;
    private String FilmOyunculari;
    private String FilmOzeti;
    private String FilmResi;
	public String getFilmAdi() {
		return FilmAdi;
	}
	public void setFilmAdi(String filmAdi) {
		FilmAdi = filmAdi;
	}
	public String getFilmTuru() {
		return FilmTuru;
	}
	public void setFilmTuru(String filmTuru) {
		FilmTuru = filmTuru;
	}
	public String getFilmSuresi() {
		return FilmSuresi;
	}
	public void setFilmSuresi(String filmSuresi) {
		FilmSuresi = filmSuresi;
	}
	public String getFilmFragmani() {
		return FilmFragmani;
	}
	public void setFilmFragmani(String filmFragmani) {
		FilmFragmani = filmFragmani;
	}
	public String getFilmOyunculari() {
		return FilmOyunculari;
	}
	public void setFilmOyunculari(String filmOyunculari) {
		FilmOyunculari = filmOyunculari;
	}
	public String getFilmOzeti() {
		return FilmOzeti;
	}
	public void setFilmOzeti(String filmOzeti) {
		FilmOzeti = filmOzeti;
	}
	public String getFilmResim() {
		return FilmResi;
	}
	public void setFilmResim(String filmResi) {
		FilmResi = filmResi;
	}
}
